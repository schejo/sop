package MD;

public class ReporteBuquesFechaMd {

    private String nom_buque;
    private String num_arribo;
    private String anio_arribo;
    private String f_atraque;
    private String tipo_buque;
    private String descripcion;
    private String tonelaje;
    private String bultos;

    public String getNom_buque() {
        return nom_buque;
    }

    public void setNom_buque(String nom_buque) {
        this.nom_buque = nom_buque;
    }

    public String getNum_arribo() {
        return num_arribo;
    }

    public void setNum_arribo(String num_arribo) {
        this.num_arribo = num_arribo;
    }

    public String getAnio_arribo() {
        return anio_arribo;
    }

    public void setAnio_arribo(String anio_arribo) {
        this.anio_arribo = anio_arribo;
    }

    public String getF_atraque() {
        return f_atraque;
    }

    public void setF_atraque(String f_atraque) {
        this.f_atraque = f_atraque;
    }

    public String getTipo_buque() {
        return tipo_buque;
    }

    public void setTipo_buque(String tipo_buque) {
        this.tipo_buque = tipo_buque;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    
    public String getTonelaje() {
        return tonelaje;
    }

    public void setTonelaje(String tonelaje) {
        this.tonelaje = tonelaje;
    }

    public String getBultos() {
        return bultos;
    }

    public void setBultos(String bultos) {
        this.bultos = bultos;
    }

}
