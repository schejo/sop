package MD;

public class ReporteCitasPlaniMd {

    private String anio_arribo;
    private String num_arribo;
    private String nombre_buque;
    private String eta_cita;
    private String fecha_hora;
    private String observaciones;
    private String hras_plani;
    private String nom_naviera;

    public String getAnio_arribo() {
        return anio_arribo;
    }

    public void setAnio_arribo(String anio_arribo) {
        this.anio_arribo = anio_arribo;
    }

    public String getNum_arribo() {
        return num_arribo;
    }

    public void setNum_arribo(String num_arribo) {
        this.num_arribo = num_arribo;
    }

    public String getNombre_buque() {
        return nombre_buque;
    }

    public void setNombre_buque(String nombre_buque) {
        this.nombre_buque = nombre_buque;
    }

    public String getEta_cita() {
        return eta_cita;
    }

    public void setEta_cita(String eta_cita) {
        this.eta_cita = eta_cita;
    }

    public String getFecha_hora() {
        return fecha_hora;
    }

    public void setFecha_hora(String fecha_hora) {
        this.fecha_hora = fecha_hora;
    }

    public String getObservaciones() {
        return observaciones;
    }

    public void setObservaciones(String observaciones) {
        this.observaciones = observaciones;
    }

    public String getHras_plani() {
        return hras_plani;
    }

    public void setHras_plani(String hras_plani) {
        this.hras_plani = hras_plani;
    }

    public String getNom_naviera() {
        return nom_naviera;
    }

    public void setNom_naviera(String nom_naviera) {
        this.nom_naviera = nom_naviera;
    }

}
