package MD;

public class ReporteServiciosParticularesMd {

    private String anio_arribo;
    private String numero_arribo;
    private String nom_buque;
    private String fecha_atraque;
    private String particular;
    private String cod_servicio;
    private String desc_servicio;
    private String factura;
    private String fecha_inicio;
    private String fecha_fin;
    private String observacion;

    public String getAnio_arribo() {
        return anio_arribo;
    }

    public void setAnio_arribo(String anio_arribo) {
        this.anio_arribo = anio_arribo;
    }

    public String getNumero_arribo() {
        return numero_arribo;
    }

    public void setNumero_arribo(String numero_arribo) {
        this.numero_arribo = numero_arribo;
    }

    public String getNom_buque() {
        return nom_buque;
    }

    public void setNom_buque(String nom_buque) {
        this.nom_buque = nom_buque;
    }

    public String getFecha_atraque() {
        return fecha_atraque;
    }

    public void setFecha_atraque(String fecha_atraque) {
        this.fecha_atraque = fecha_atraque;
    }

    public String getParticular() {
        return particular;
    }

    public void setParticular(String particular) {
        this.particular = particular;
    }

    public String getCod_servicio() {
        return cod_servicio;
    }

    public void setCod_servicio(String cod_servicio) {
        this.cod_servicio = cod_servicio;
    }

    public String getDesc_servicio() {
        return desc_servicio;
    }

    public void setDesc_servicio(String desc_servicio) {
        this.desc_servicio = desc_servicio;
    }

    public String getFactura() {
        return factura;
    }

    public void setFactura(String factura) {
        this.factura = factura;
    }

    public String getFecha_inicio() {
        return fecha_inicio;
    }

    public void setFecha_inicio(String fecha_inicio) {
        this.fecha_inicio = fecha_inicio;
    }

    public String getFecha_fin() {
        return fecha_fin;
    }

    public void setFecha_fin(String fecha_fin) {
        this.fecha_fin = fecha_fin;
    }

    public String getObservacion() {
        return observacion;
    }

    public void setObservacion(String observacion) {
        this.observacion = observacion;
    }

}
