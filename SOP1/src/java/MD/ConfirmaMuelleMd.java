public class ConfirmaMuelleMd {
    private String anio;
    private String manif;
    private String tipo;
    private String estado;
    private String correlaBl;
    private String puerto;
    private String nombre;
    private String contenedor;

    public ConfirmaMuelleMd() {
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }

    public String getCorrelaBl() {
        return correlaBl;
    }

    public void setCorrelaBl(String correlaBl) {
        this.correlaBl = correlaBl;
    }

    public String getAnio() {
        return anio;
    }

    public void setAnio(String anio) {
        this.anio = anio;
    }

    public String getManif() {
        return manif;
    }

    public void setManif(String manif) {
        this.manif = manif;
    }

    public String getPuerto() {
        return puerto;
    }

    public void setPuerto(String puerto) {
        this.puerto = puerto;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getContenedor() {
        return contenedor;
    }

    public void setContenedor(String contenedor) {
        this.contenedor = contenedor;
    }

    
    
    
}
