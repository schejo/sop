
package MD;

public class ReporteRayosXMd {
    
    private String correlativo;
    private String ncontenedor;
    private String naviera;
    private String buque;
    private String actividad;
    private String act;

    public ReporteRayosXMd() {
    }

    public String getNcontenedor() {
        return ncontenedor;
    }

    public void setNcontenedor(String ncontenedor) {
        this.ncontenedor = ncontenedor;
    }

    public String getNaviera() {
        return naviera;
    }

    public void setNaviera(String naviera) {
        this.naviera = naviera;
    }

    public String getCorrelativo() {
        return correlativo;
    }

    public void setCorrelativo(String correlativo) {
        this.correlativo = correlativo;
    }

    public String getBuque() {
        return buque;
    }

    public void setBuque(String buque) {
        this.buque = buque;
    }

    public String getActividad() {
        return actividad;
    }

    public void setActividad(String actividad) {
        this.actividad = actividad;
    }

    public String getAct() {
        return act;
    }

    public void setAct(String act) {
        this.act = act;
    }

  
    
    
    
}
