public class MantenimientoPlanificacionMd {
    
    private String idPlnificacion;
    private String anio;
    private String tipo;
    private String empresa;
    private String mes;
    private String retiene;
    private String estado;

    public MantenimientoPlanificacionMd() {
    }

    public String getIdPlnificacion() {
        return idPlnificacion;
    }

    public void setIdPlnificacion(String idPlnificacion) {
        this.idPlnificacion = idPlnificacion;
    }

    public String getAnio() {
        return anio;
    }

    public void setAnio(String anio) {
        this.anio = anio;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    public String getEmpresa() {
        return empresa;
    }

    public void setEmpresa(String empresa) {
        this.empresa = empresa;
    }

    public String getMes() {
        return mes;
    }

    public void setMes(String mes) {
        this.mes = mes;
    }

    public String getRetiene() {
        return retiene;
    }

    public void setRetiene(String retiene) {
        this.retiene = retiene;
    }

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }
    
    
    
}
